class Date
{
	int date;
	int month;
	int year;
	Date()
	{
		date=0;
		month=0;
		year=0;
	}
	void setdate(int date)
	{
		this.date=date;
	}
	
	int getdate()
	{
		return date;
	}
	
	void setmonth(int month)
	{
		this.month=month;
	}
	
	int getmonth()
	{
		return month;
	}

	void setyear(int year)
	{
		this.year=year;
	}
	
	int getyear()
	{
		return year;
	}
}